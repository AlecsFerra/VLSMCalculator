package it.alecsferra.vlsmc.core;

public class InvalidIPv4Exception extends RuntimeException {

    public InvalidIPv4Exception() {
        super();
    }

    public InvalidIPv4Exception(String message) {
        super(message);
    }

    public InvalidIPv4Exception(String message, Throwable cause) {
        super(message, cause);
    }

    public InvalidIPv4Exception(Throwable cause) {
        super(cause);
    }
}
